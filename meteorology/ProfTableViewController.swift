        if u.Nick == nickLable.text {
            return
        }
        
        nickLable.text = u.Nick
        dispatch_async(dispatch_get_global_queue(0, 0)) {
            var nsd = NSData(contentsOfURL: NSURL(string: u.Icon!)!, options: NSDataReadingOptions.DataReadingUncached, error: nil)
            dispatch_async(dispatch_get_main_queue()) {
                if nsd != nil {
                    self.iconImg.image = UIImage(data: nsd!)
                } else {
                    self.iconImg.image = UIImage(named: "default_icon")
                }
            }
        }
